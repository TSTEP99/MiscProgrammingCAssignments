.ORIG x3000

AND R6,R6,#0 ;r for counting

LDI R1,STARTING_ADDRESS ; value of minimum


ARRAY_LOOP ADD R7,R6,#-15
BRp STOP
LD R3,STARTING_ADDRESS ;gets starting address
ADD R3,R6,R3

LDR R3,R3,#0

NOT R4,R3
ADD R4,R4,#1

ADD R4,R1,R4

BRnz NOT_GREATER

AND R7,R7,#0

ADD R1,R1,#0
BRzp SKIP_FIRST_ADD
ADD R7,R7,#1

SKIP_FIRST_ADD ADD R4,R4,#0
BRnz SKIP_SECOND_ADD
ADD R7,R7,#1

SKIP_SECOND_ADD NOT R4,R3
ADD R4,R4,#1
BRzp SKIP_THIRD_ADD
ADD R7,R7,#1

SKIP_THIRD_ADD ADD R7,R7,#-3
BRz NOT_GREATER



ADD R1,R3,#0


NOT_GREATER ADD R6,R6,#1
BRnzp ARRAY_LOOP

STOP ADD R5,R1,#0
HALT
STARTING_ADDRESS .FILL x5000
.END